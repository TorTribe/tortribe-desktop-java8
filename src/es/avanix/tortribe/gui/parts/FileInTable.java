
package es.avanix.tortribe.gui.parts;

/**
 *
 * @author juanjo
 */
public class FileInTable {
    
    private String hash;
    private String name;

    public FileInTable(String hash, String name) {
        this.hash = hash;
        this.name = name;
    }

    public String getHash() {
        return hash;
    }

    public String getName() {
        return name;
    }
    
    
    
}
